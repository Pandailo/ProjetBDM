    private void button_validerActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_button_validerActionPerformed
    {//GEN-HEADEREND:event_button_validerActionPerformed
        try
        {
            int index=0;
            Connection con=connexionUtils.getInstance().getConnexion();
            con.setAutoCommit(false);
            Statement s=null;
            
            s = con.createStatement();
            OracleResultSet rs=null;
            
            rs=(OracleResultSet) s.executeQuery("INSERT INTO PBDM_Film VALUES("+index+",'"+this.field_date.getText()+"','"+this.field_titre.getText()+"','"+this.edition_synopsis.getText()+"','"+this.field_genre.getText()+"',ORDSYS.ORDImage.init(),ORDSYS.ORDVideo.init(),ORDSYS.ORDAudio.init(), (SELECT REF(r) FROM PBDM_Realisateur r WHERE id="+this.idR+"))");
            index=-1;
            rs=(OracleResultSet)s.executeQuery("select id, image, bandeA, bandeO from PBDM_Film where nom='"+this.field_titre.getText()+"' for update");
            while(rs.next())
            {
                index=rs.getInt(1);
                OraclePreparedStatement or=(OraclePreparedStatement) con.prepareStatement("INSERT INTO THE(SELECT filmsR FROM PBDM_Realisateur r WHERE r.id="+this.idR+") SELECT REF(f) FROM PBDM_Film f WHERE f.id=?");
                or.setInt(1, index);
                or.execute();
                or.close();
                OrdImage imgObj= (OrdImage)rs.getORAData(2,OrdImage.getORADataFactory());
                OrdVideo vidObj= (OrdVideo)rs.getORAData(3,OrdVideo.getORADataFactory());
                OrdAudio sonObj= (OrdAudio)rs.getORAData(4,OrdAudio.getORADataFactory());
                String fich=this.cheminPhoto;
                String vid=this.cheminBA;
                String son=this.cheminBO;
                if(!(this.cheminPhoto.equals("")))
                        imgObj.loadDataFromFile(fich);
                if(!(this.cheminBA.equals("")))
                    vidObj.loadDataFromFile(vid);
                if(!(this.cheminBO.equals("")))
                    sonObj.loadDataFromFile(son);
                byte[] ctx[] = new byte [4000][1];
                imgObj.setProperties();
                if(!(this.cheminBA.equals("")))
                   vidObj.setProperties(ctx);
                if(!(this.cheminBO.equals("")))
                    sonObj.setProperties(ctx);
                OraclePreparedStatement stmt1;
                if((!this.cheminBA.equals(""))&&(!this.cheminBO.equals("")))
                {
                    stmt1=(OraclePreparedStatement)con.prepareStatement("update PBDM_Film set image=?,bandeA=?,bandeO=? where id="+index);
                    stmt1.setORAData(1,imgObj);
                    stmt1.setORAData(2,vidObj);
                    stmt1.setORAData(3,sonObj);
                }
                else if((!this.cheminBA.equals("")))
                {
                    stmt1=(OraclePreparedStatement)con.prepareStatement("update PBDM_Film set image=?,bandeA=? where id="+index);
                    stmt1.setORAData(1,imgObj);
                    stmt1.setORAData(2,vidObj);
                }
                else
                {
                    stmt1=(OraclePreparedStatement)con.prepareStatement("update PBDM_Film set image=? where id="+index);
                    stmt1.setORAData(1,imgObj);
                }
                stmt1.execute();
                stmt1.close();   
            }
            rs=(OracleResultSet)s.executeQuery("ALTER INDEX PBDM_indexF REBUILD");
            rs=(OracleResultSet)s.executeQuery("ALTER INDEX PBDM_indexSF REBUILD");
            OraclePreparedStatement st=null;
            if(!this.l_acteurs.isEmpty())
            {
                for(int i=0;i<this.l_acteurs.size();i++)
                {
                    st=(OraclePreparedStatement) con.prepareStatement("INSERT INTO PBDM_MedVidActeur VALUES((SELECT REF(a) FROM PBDM_Acteur a WHERE id=?),(SELECT REF(f) FROM PBDM_Film f WHERE id=?))");
                    st.setInt(1,this.l_acteurs.get(i));
                    st.setInt(2,index);
                    st.execute();
                    st.close();
                }
            }
            rs.close();
            s.close();
            
            con.commit();
        }
        catch (SQLException | IOException | ClassNotFoundException ex)
        {
            Logger.getLogger(frame_ajout_film.class.getName()).log(Level.SEVERE, null, ex);
        }
        this.dispose();
    }//GEN-LAST:event_button_validerActionPerformed

